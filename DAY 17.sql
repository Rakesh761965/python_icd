-- TASK 3: Update Bonus Using IF Condition
DECLARE
v_sal EMM.SALARY%TYPE;
BEGIN
SELECT SALARY INTO v_sal FROM EMM WHERE EMP_ID = 101;
IF v_sal > 40000 THEN
UPDATE EMM SET BONUS = 5000 WHERE EMP_ID = 101;
ELSE
UPDATE EMM SET BONUS = 2000 WHERE EMP_ID = 101;
END IF;
COMMIT;
END;
