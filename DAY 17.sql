-- TASK 4: Insert Multiple Employees Using LOOP
DECLARE
v_id NUMBER := 201;
BEGIN
LOOP
INSERT INTO EMM (EMP_ID, EMP_NAME, SALARY, DEPT_ID, BONUS, STATUS, HIRE_DATE)
VALUES (v_id, 'Employee'||v_id, 30000, 10, 1000, 'ACTIVE', SYSDATE);
v_id := v_id + 1;
EXIT WHEN v_id > 205;
END LOOP;
COMMIT;
END;
