----USE NUMERIC FUN
SELECT PRODUCT_NAME, PRICE, ROUND(PRICE) AS ROUND_PRICE, CEIL(PRICE) AS CEIL_PRICE, FLOOR(PRICE) AS FLOOR_PRICE, MOD(STOCK, 2) AS STOCK_MOD FROM PROD;

----USE STRING FUN
SELECT LOWER(EMAIL) AS LOWERCASE_EMAIL, LENGTH(CUSTOMER_NAME) AS NAME_LENGTH, SUBSTR(CUSTOMER_NAME, 1, 5) AS FIRST_5_CHARS FROM CUSTOM;

----DATE FUN
SELECT ORDER_ID, ORDER_DATE, ROUND(MONTHS_BETWEEN(SYSDATE, ORDER_DATE)) AS MONTHS_OLD FROM ORDERSSS;

----CONVERSION FUn
SELECT TO_CHAR(ORDER_DATE, 'DD-MON-YYYY') AS FORMATTED_DATE FROM ORDERSSS;

----NVL FUN
SELECT DELIVERY_ID, NVL(TO_CHAR(DELIVERY_DATE), '02-MAR-2000') AS DELIVERY_DATE FROM DEL;

----TOTAL REVENUE
SELECT SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE FROM ORDERSSS;

----AVERAGE PRODUCT PRICE
SELECT AVG(PRICE) AS AVG_PRICE FROM PROD;

----HIGH SELLING PROD
SELECT PRODUCT_ID, SUM(QUANTITY) AS TOTAL_QUANTITY FROM ORDER_ITEMS GROUP BY PRODUCT_ID HAVING SUM(QUANTITY) = (SELECT MAX(SUM(QUANTITY)) FROM ORDER_ITEMS GROUP BY PRODUCT_ID);

----REVENUE BY ORDER STATUS
SELECT O.ORDER_ID, D.DELIVERY_STATUS, O.TOTAL_AMOUNT FROM ORDERSSS O JOIN DEL D ON O.ORDER_ID = D.ORDER_ID;

----CUSTOMER SPENDING MORE THAN 90000
SELECT CUSTOMER_ID, SUM(TOTAL_AMOUNT) AS TOTAL_SPENT FROM ORDERSSS GROUP BY CUSTOMER_ID HAVING SUM(TOTAL_AMOUNT) > 90000;

----CUSTOMER BY SPENDING
SELECT CUSTOMER_ID, SUM(TOTAL_AMOUNT) AS TOTAL_SPENT FROM ORDERSSS GROUP BY CUSTOMER_ID ORDER BY TOTAL_SPENT DESC FETCH FIRST 3 ROWS ONLY;

----SELLER WISE REVENUE
SELECT S.SELLER_NAME, SUM(OI.QUANTITY * OI.ITEM_PRICE) AS REVENUE FROM SELL S JOIN PROD P ON S.SELLER_ID = P.SELLER_ID JOIN ORDER_ITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID GROUP BY S.SELLER_NAME;

----FAILED PAYMENT
SELECT * FROM PAY WHERE PAYMENT_STATUS = 'Failed';

----PENDING DELIVERIES
SELECT * FROM DELIVERY WHERE DELIVERY_STATUS != 'Delivered';

----BEST RATED PRODUCT
SELECT PRODUCT_ID, AVG(RATING) AS AVG_RATING FROM REVIEWSS GROUP BY PRODUCT_ID ORDER BY AVG(RATING) DESC;

----CATEGORY WISE SALES
SELECT C.CATEGORY_NAME, SUM(OI.QUANTITY * OI.ITEM_PRICE) AS SALES FROM CATEGORIES C JOIN PROD P ON C.CATEGORY_ID = P.CATEGORY_ID JOIN ORDER_ITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID GROUP BY C.CATEGORY_NAME;

----MONTHLY REVENUE
SELECT TO_CHAR(ORDER_DATE, 'MON-YYYY') AS MONTH, SUM(TOTAL_AMOUNT) AS REVENUE FROM ORDERSSS GROUP BY TO_CHAR(ORDER_DATE, 'MON-YYYY');

----CREATE INVENTORY WAREHOUSE TABLE
CREATE TABLE INVENTORY_WAREHOUSE (WAREHOUSE_ID NUMBER PRIMARY KEY, PRODUCT_ID NUMBER REFERENCES PROD(PRODUCT_ID), STOCK_QUANTITY NUMBER, WAREHOUSE_LOCATION VARCHAR2(100), LAST_UPDATED DATE);

----CREATE COUPONS TABLE
CREATE TABLE COUPONS (COUPON_ID NUMBER PRIMARY KEY, COUPON_CODE VARCHAR2(50) UNIQUE, DISCOUNT_PERCENT NUMBER, EXPIRY_DATE DATE, IS_ACTIVE VARCHAR2(10));

----CREATE EMPLOYEE TABLE
CREATE TABLE EMPLOYEE (EMP_ID NUMBER PRIMARY KEY, EMP_NAME VARCHAR2(100), DEPARTMENT VARCHAR2(50), SALARY NUMBER, MANAGER_ID NUMBER);

----CREATE RETURNS TABLE
CREATE TABLE RETURNS (RETURN_ID NUMBER PRIMARY KEY, ORDER_ID NUMBER REFERENCES ORDERSSS(ORDER_ID), PRODUCT_ID NUMBER REFERENCES PROD(PRODUCT_ID), RETURN_DATE DATE, REFUND_AMOUNT NUMBER, RETURN_REASON VARCHAR2(200));

----CREATE SHIPMENT TABLE
CREATE TABLE SHIPMENT (SHIPMENT_ID NUMBER PRIMARY KEY, ORDER_ID NUMBER REFERENCES ORDERSSS(ORDER_ID), CARRIER_NAME VARCHAR2(100), TRACKING_NUMBER VARCHAR2(100), SHIPMENT_STATUS VARCHAR2(50));

----INVENTORY ANALYSIS 
SELECT I.WAREHOUSE_ID, I.PRODUCT_ID, P.PRODUCT_NAME, I.STOCK_QUANTITY FROM INVENTORY_WAREHOUSE I JOIN PROD P ON I.PRODUCT_ID = P.PRODUCT_ID WHERE I.STOCK_QUANTITY < 20;

----EXPIRED COUPONS
SELECT * FROM COUPONS WHERE EXPIRY_DATE < SYSDATE;

----ORDERS USING COUPONS 
-- ALTER TABLE ORDERSSS ADD COUPON_ID NUMBER REFERENCES COUPONS(COUPON_ID);
SELECT O.ORDER_ID, C.COUPON_CODE, O.TOTAL_AMOUNT FROM ORDERSSS O LEFT JOIN COUPONS C ON O.COUPON_ID = C.COUPON_ID;

----EMPLOYEE HIERARCHY 
SELECT E1.EMP_NAME AS EMPLOYEE, E2.EMP_NAME AS MANAGER FROM EMPLOYEE E1 LEFT JOIN EMPLOYEE E2 ON E1.MANAGER_ID = E2.EMP_ID;

----HIGHEST PAID EMPLOYEE IN EACH DEPARTMENT
SELECT DEPARTMENT, EMP_NAME, SALARY FROM EMPLOYEE E1 WHERE SALARY = (SELECT MAX(SALARY) FROM EMPLOYEE E2 WHERE E2.DEPARTMENT = E1.DEPARTMENT);

----ORDERS RETURNED BY CUSTOMERS
SELECT R.RETURN_ID, O.CUSTOMER_ID, C.CUSTOMER_NAME, R.ORDER_ID, R.PRODUCT_ID, R.RETURN_DATE, R.REFUND_AMOUNT FROM RETURNS R JOIN ORDERSSS O ON R.ORDER_ID = O.ORDER_ID JOIN CUSTOM C ON O.CUSTOMER_ID = C.CUSTOMER_ID;

----SHIPMENT TRACKING DASHBOARD
SELECT S.SHIPMENT_ID, S.ORDER_ID, S.CARRIER_NAME, S.TRACKING_NUMBER, S.SHIPMENT_STATUS, D.DELIVERY_STATUS FROM SHIPMENT S JOIN DELIVERY D ON S.ORDER_ID = D.ORDER_ID;

----TOP RATED SELLER
SELECT S.SELLER_ID, S.SELLER_NAME, AVG(R.RATING) AS AVG_RATING FROM SELL S JOIN PROD P ON S.SELLER_ID = P.SELLER_ID JOIN REVIEWSS R ON P.PRODUCT_ID = R.PRODUCT_ID GROUP BY S.SELLER_ID, S.SELLER_NAME ORDER BY AVG_RATING DESC FETCH FIRST 1 ROW ONLY;

----REVENUE AFTER DISCOUNT
SELECT P.PRODUCT_NAME, P.PRICE, P.DISCOUNT, OI.QUANTITY, (OI.ITEM_PRICE * OI.QUANTITY) AS REVENUE_BEFORE_DISCOUNT, (OI.ITEM_PRICE * OI.QUANTITY) * (1 - P.DISCOUNT/100) AS REVENUE_AFTER_DISCOUNT FROM PROD P JOIN ORDER_ITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID;

