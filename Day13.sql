-- CUSTOMERS
CREATE TABLE CUSTOM (CUSTOMER_ID NUMBER PRIMARY KEY,CUSTOMER_NAME VARCHAR2(100),EMAIL VARCHAR2(100),
CITY VARCHAR2(100),REGISTRATION_DATE DATE,
MEMBERSHIP_TYPE VARCHAR2(20));

-----seller
CREATE TABLE SELL (SELLER_ID NUMBER PRIMARY KEY,SELLER_NAME VARCHAR2(100),
CITY VARCHAR2(50),
RATING NUMBER(2,1));

-- PRODUCTS
CREATE TABLE PROD (PRODUCT_ID NUMBER PRIMARY KEY,
PRODUCT_NAME VARCHAR2(200),CATEGORY_ID NUMBER,SELLER_ID NUMBER,
PRICE NUMBER(10,2),STOCK NUMBER,DISCOUNT NUMBER);

-- ORDERS
CREATE TABLE ORDERSSS (ORDER_ID NUMBER PRIMARY KEY,CUSTOMER_ID NUMBER,
ORDER_DATE DATE,TOTAL_AMOUNT NUMBER(10,2));


-- DELIVERY 
CREATE TABLE DEL (DELIVERY_ID NUMBER PRIMARY KEY,ORDER_ID NUMBER,DELIVERY_STATUS VARCHAR2(20),DELIVERY_DATE DATE);

----CATEGERIOS
CREATE TABLE CATEGORIES (CATEGORY_ID NUMBER PRIMARY KEY,CATEGORY_NAME VARCHAR2(100));

-----ORDER ITEM
CREATE TABLE ORDER_ITEMS (ITEM_ID NUMBER PRIMARY KEY,ORDER_ID NUMBER REFERENCES ORDERSSS(ORDER_ID),
PRODUCT_ID NUMBER REFERENCES PROD(PRODUCT_ID),QUANTITY NUMBER,
ITEM_PRICE NUMBER(18,2));

----PAYMENTS

CREATE TABLE PAY (PAYMENT_ID NUMBER PRIMARY KEY,ORDER_ID NUMBER REFERENCES ORDERSSS(ORDER_ID),
PAYMENT_METHOD VARCHAR2(20),PAYMENT_STATUS VARCHAR2(20),PAYMENT_DATE DATE);

-----DELIVERY
CREATE TABLE DELIVERYY (DELIVERY_ID NUMBER PRIMARY KEY,ORDER_ID NUMBER REFERENCES ORDERSSS(ORDER_ID),
DELIVERY_STATUS VARCHAR2(30),DELIVERY_DATE DATE);

-----REWIES
CREATE TABLE REVIEWSS (REVIEW_ID NUMBER PRIMARY KEY,CUSTOMER_ID NUMBER REFERENCES CUSTOM(CUSTOMER_ID),
PRODUCT_ID NUMBER REFERENCES PROD(PRODUCT_ID),RATING NUMBER(1),REVIEW_TEXT VARCHAR2(500));

